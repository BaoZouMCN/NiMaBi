public class CoinGeneratorException extends Exception {

    public CoinGeneratorException() {
        super();
    }

    public CoinGeneratorException(String message) {
        super(message);
    }

    public CoinGeneratorException(String message, Throwable cause) {
        super(message, cause);
    }

    public CoinGeneratorException(Throwable cause) {
        super(cause);
    }
}
